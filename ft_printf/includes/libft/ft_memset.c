#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	size_t	i;
	char	*s_mem;

	s_mem = b;
	i = 0;
	while (i < n)
	{
		s_mem[i] = c;
		i++;
	}
	b = s_mem;
	return (b);
}
